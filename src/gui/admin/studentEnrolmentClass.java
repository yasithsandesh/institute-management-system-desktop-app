    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String studentId = jTextField1.getText();
        String className = String.valueOf(jComboBox3.getSelectedItem());
        if (className.matches("Select Class")) {
            JOptionPane.showMessageDialog(this, "Select Class", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            String classId = classIdMap.get(className);

            try {
                 Boolean find = didectData(classId,studentId);
                if(!find){
                 MySQL.execute("INSERT INTO `student_has_class`(`student_sno`,`class_classNo`)VALUES('" + studentId + "','" + classId + "')");
                loadEnrolClass();
                }else{
                JOptionPane.showMessageDialog(this, "Add Class", "Warning", JOptionPane.WARNING_MESSAGE);
                }
               

            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_jButton2ActionPerformed

