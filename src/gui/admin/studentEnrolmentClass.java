    private void loadClass() {

        try {
            ResultSet classRs = MySQL.execute("SELECT * FROM `class`");
            Vector<String> classVector = new Vector<>();
            classVector.add("Select Class");
            while (classRs.next()) {
                classVector.add(classRs.getString("className"));
                classIdMap.put(classRs.getString("className"), classRs.getString("classNo"));
            }

            DefaultComboBoxModel model = new DefaultComboBoxModel(classVector);
            jComboBox3.setModel(model);
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    private void loadEnrolClass() {

        String studentId = jTextField1.getText();
        try {
            ResultSet enClassRs = MySQL.execute("SELECT * FROM `student_has_class`"
                    + " INNER JOIN `class`ON `student_has_class`.`class_classNo`=`class`.`classNo`"
                    + " INNER JOIN `teacher` ON `class`.`teacher_tno`=`teacher`.`tno`"
                    + " INNER JOIN `days` ON `class`.`days_day_id`=`days`.`day_id` INNER JOIN `subject` ON `class`.`subject_subno`=`subject`.`subno`  WHERE `student_sno`='" + studentId + "'");
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (enClassRs.next()) {
                Vector<String> classVector = new Vector<>();
                classVector.add(enClassRs.getString("teacher.fname") + ' ' + enClassRs.getString("teacher.lname"));
                classVector.add(enClassRs.getString("className"));
                classVector.add(enClassRs.getString("rdate"));
                classVector.add(enClassRs.getString("start_time") + '-' + enClassRs.getString("end_time"));
                classVector.add(enClassRs.getString("day_name"));
                classVector.add(enClassRs.getString("subject.sub_name"));

                model.addRow(classVector);
                jTable1.setModel(model);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    private boolean didectData(String classId,String stuno) {

        try {
            ResultSet classRs = MySQL.execute("SELECT * FROM `student_has_class` WHERE `class_classNo`='" + classId + "' AND `student_sno`='"+stuno+"'");
            if (classRs.next()) {
                return true;
            } else {
                return false;
            }
        } catch (Exception e) {
            e.printStackTrace();
            return false;
        }

    }

